SELECT
  D.GROUP_NUMBER 
 ,D.HEADER_STATUS ST_HEADER
 ,( SELECT G.NAME FROM V$ASM_DISKGROUP G WHERE G.GROUP_NUMBER = D.GROUP_NUMBER ) GROUP_NAME
 ,D.DISK_NUMBER  
 ,D.NAME DISK_NAME
 --,D.LABEL LABEL_NAME
 ,D.PATH
 ,D.FAILGROUP

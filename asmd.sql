SELECT 
  dg.name GROUP_NAME
 ,SUBSTR(c.instance_name,1,12) AS instance
 ,rpad(c.db_name,12,' ')  DB_CLIENT
 ,SUBSTR(c.SOFTWARE_VERSION,1,12) AS software
 ,SUBSTR(c.COMPATIBLE_VERSION,1,12) AS compatible 
